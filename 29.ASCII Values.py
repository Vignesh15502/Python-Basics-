'''
ASCII stands for American Standard 
Code for Information Interchange. 
It is a character encoding standard 
that uses numbers from 0 to 127 to represent 
English characters. For example, ASCII code 
for the character A is 65, and 90 is for Z. 
Similarly, ASCII code 97 is for a, and 122 is for z.
'''

