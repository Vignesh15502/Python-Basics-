# Logical Operators in python

"""
and
or
not
"""
a=20
print(a>=10 and a<=20)

a=25
print(a>=10 or a<=20)

a=25
print(not (a>=10 and a<=20))

'''
Python has three Boolean operators, 
or logical operators: and , or , and not . 
You can use them to check if certain conditions 
are met before deciding the execution path your 
programs will follow.
'''