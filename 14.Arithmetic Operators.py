# Arithmetic Operators

'''
Addition	      +	Adds one operand to the other
Subtraction       -	Subtracts the second operand from the first
Multiplication	  *	Multiplies one operand by the other
Division	      /	Divides the first operand by the second
Modulo	          %	Divides the first INTEGER operand by the second, 
                    and returns the remainder
Exponentiation	 **	Lets you refer to a number in terms 
                    of a base value and an exponent
Floor Division   // the quotient is rounded down to the nearest integer value
'''
a=123
b=10
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a//b)
print(a%b)
print(a**b)					