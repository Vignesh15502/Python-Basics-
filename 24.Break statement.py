#Break statement

i=1
while i<=20:
	if i==7:
		break;
	print(i)
	i+=1
    
'''
'Break' in Python is a loop control statement. 
It is used to control the sequence of the loop. 
Suppose you want to terminate a loop and skip 
to the next code after the loop; 
break will help you do that. A typical scenario 
of using the Break in Python is when an external 
condition triggers the loop's termination.
'''