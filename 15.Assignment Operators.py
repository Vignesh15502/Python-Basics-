# Assignment Operators
'''
=	Assignment Operator	                 a=b+c   (=)is Storing data
+=	Addition Assignment Operator         a+=b or a=a+b
-=	Subtraction Assignment Operator	     a-=b or a=a-b
*=	Multiplication Assignment Operator	 a*=b or a=a*b
/=	Division Assignment Operator         a/=b or a=a/b
%=	Modulus Assignment Operator		     a%=b or a=a%b
**=	Exponentiation Assignment Operator	 a**=b or a=a**b
//=	Floor Division Assignment Operator	 a//=b or a=a//b
'''
a=125
print(a)
a+=5
a-=10
a*=10
a/=10
a%=10
a**10
a//=10
